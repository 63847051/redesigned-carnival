#!/bin/bash
###############################################################################
# Verification Loops - 验证任务进度
# 功能: 定期检查任务状态，发现偏差及时纠正
###############################################################################

echo "🔍 Verification Loops - 开始验证..."
echo "⏰ 时间: $(date '+%Y-%m-%d %H:%M:%S')"
echo ""

# 获取今天的日期
today=$(date +%Y-%m-%d)
log_file="/root/.openclaw/workspace/memory/$today.md"

# 检查日志文件是否存在
if [ ! -f "$log_file" ]; then
  echo "⚠️  今天的日志不存在: $log_file"
  echo "💡 建议: 先创建今天的日志"
  exit 1
fi

echo "📖 验证今天的日志: $log_file"
echo ""

###############################################################################
# 1. 检查任务完成状态
###############################################################################

echo "## ✅ 任务完成状态"

# 检查是否有"完成"标记
if grep -q "✅ 完成\|完成✅\|超级完成\|全部完成" "$log_file"; then
  echo "✅ 任务已完成"
  grep "✅ 完成\|完成✅\|超级完成\|全部完成" "$log_file" | tail -5
else
  echo "⚠️  任务仍在进行中"
fi

echo ""

###############################################################################
# 2. 检查错误状态
###############################################################################

echo "## 🚨 错误状态"

# 检查是否有"错误"标记
if grep -q "❌\|错误\|失败\|问题" "$log_file"; then
  echo "🚨 发现错误，需要关注:"
  grep "❌\|错误\|失败\|问题" "$log_file" | tail -5
  echo ""
  echo "💡 建议:"
  echo "  1. 分析错误原因"
  echo "  2. 制定解决方案"
  echo "  3. 实施并验证"
else
  echo "✅ 很好！今天没有错误"
fi

echo ""

###############################################################################
# 3. 检查待办事项
###############################################################################

echo "## 📝 待办事项"

# 检查是否有待办事项
if grep -q "^\- \[ \]\|待办\|TODO\|待完成" "$log_file"; then
  echo "📝 发现待办事项:"
  grep "^\- \[ \]\|待办\|TODO\|待完成" "$log_file" | tail -10
  echo ""
  echo "💡 建议:"
  echo "  1. 优先处理重要事项"
  echo "  2. 分解复杂任务"
  echo "  3. 及时更新状态"
else
  echo "✅ 很好！没有待办事项"
fi

echo ""

###############################################################################
# 4. 检查学习进度
###############################################################################

echo "## 🧠 学习进度"

# 检查是否有学习记录
if grep -q "学习\|掌握\|理解\|领悟" "$log_file"; then
  echo "🧠 学习进度:"
  grep "学习\|掌握\|理解\|领悟" "$log_file" | tail -5
  learning_count=$(grep -c "学习\|掌握\|理解\|领悟" "$log_file")
  echo ""
  echo "📊 学习统计: $learning_count 个学习记录"
else
  echo "⚠️  今天没有学习记录"
  echo "💡 建议: 每天都要学习新东西"
fi

echo ""

###############################################################################
# 5. 检查工作质量
###############################################################################

echo "## 🔍 工作质量"

# 检查是否有质量标记
if grep -q "优化\|改进\|提升\|完善" "$log_file"; then
  echo "✅ 工作质量提升:"
  grep "优化\|改进\|提升\|完善" "$log_file" | tail -5
  quality_count=$(grep -c "优化\|改进\|提升\|完善" "$log_file")
  echo ""
  echo "📊 质量统计: $quality_count 个改进记录"
else
  echo "⚠️  今天没有质量改进记录"
  echo "💡 建议: 每天都要优化一些东西"
fi

echo ""

###############################################################################
# 6. 检查进度偏差
###############################################################################

echo "## 📊 进度偏差"

# 检查是否有计划
if grep -q "计划\|预计\|目标" "$log_file"; then
  echo "📊 检查进度偏差..."
  
  # 提取计划
  plans=$(grep -A 5 "计划\|预计\|目标" "$log_file" | head -20)
  
  if [ -n "$plans" ]; then
    echo "📋 原计划:"
    echo "$plans"
    echo ""
    
    # 检查是否完成
    if grep -q "✅ 完成" "$log_file"; then
      echo "✅ 按计划完成"
    else
      echo "⚠️  可能存在进度偏差"
      echo "💡 建议: 重新评估计划"
    fi
  fi
else
  echo "⚠️  今天没有明确的计划"
  echo "💡 建议: 开始工作前先制定计划"
fi

echo ""

###############################################################################
# 7. 生成验证报告
###############################################################################

echo "## 📋 验证报告"

# 创建验证报告
report_file="/root/.openclaw/workspace/verification-reports/$today.md"
mkdir -p "$(dirname "$report_file")"

cat > "$report_file" << EOF
# 验证报告 - $today

**验证时间**: $(date '+%Y-%m-%d %H:%M:%S')

---

## 任务状态

$(if grep -q "✅ 完成" "$log_file"; then
  echo "✅ 已完成"
else
  echo "⏳ 进行中"
fi)

## 错误状态

$(if grep -q "❌\|错误" "$log_file"; then
  echo "🚨 发现错误"
  grep "❌\|错误" "$log_file" | tail -5
else
  echo "✅ 无错误"
fi)

## 待办事项

$(if grep -q "^\- \[ \]" "$log_file"; then
  echo "📝 有待办事项"
  grep "^\- \[ \]" "$log_file" | tail -10
else
  echo "✅ 无待办事项"
fi)

## 学习进度

$(if grep -q "学习" "$log_file"; then
  learning_count=$(grep -c "学习" "$log_file")
  echo "🧠 $learning_count 个学习记录"
else
  echo "⚠️  无学习记录"
fi)

## 工作质量

$(if grep -q "优化\|改进" "$log_file"; then
  quality_count=$(grep -c "优化\|改进" "$log_file")
  echo "✅ $quality_count 个改进记录"
else
  echo "⚠️  无改进记录"
fi)

---

**验证完成**: $(date '+%Y-%m-%d %H:%M:%S')
EOF

echo "📄 验证报告: $report_file"

echo ""

###############################################################################
# 8. 建议和提醒
###############################################################################

echo "## 💡 建议和提醒"

# 生成个性化建议
echo ""
echo "基于今天的分析，建议:"

if ! grep -q "学习" "$log_file"; then
  echo "  ⚠️  今天没有学习记录，建议每天学习新东西"
fi

if ! grep -q "优化\|改进" "$log_file"; then
  echo "  ⚠️  今天没有质量改进，建议每天优化一些东西"
fi

if grep -q "^\- \[ \]" "$log_file"; then
  todo_count=$(grep -c "^\- \[ \]" "$log_file")
  echo "  📝 还有 $todo_count 个待办事项，建议及时完成"
fi

if grep -q "❌\|错误" "$log_file"; then
  echo "  🚨 发现错误，建议及时处理"
fi

echo ""

###############################################################################
# 完成
###############################################################################

echo "🎉 验证完成！"
echo ""
echo "📊 验证统计:"
echo "  - 任务状态: 已检查"
echo "  - 错误状态: 已检查"
echo "  - 待办事项: 已检查"
echo "  - 学习进度: 已检查"
echo "  - 工作质量: 已检查"
echo "  - 进度偏差: 已检查"
echo ""
echo "💡 下一步:"
echo "  1. 查看验证报告: cat $report_file"
echo "  2. 处理发现的问题"
echo "  3. 更新工作计划"
